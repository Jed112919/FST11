// Variable = container

/*
    let = mutable/changeable - block scope
    var = immutable/unchangeable - global scope
    const = mutable/changeable - global scope
*/

let firstName = 'Jed';
console.log("Firstname: ", firstName);

firstName = "Jannah";
console.log("Firstname:", firstName);

const birthday = "1998";
console.log("Birthday", birthday);

var lastName = "Torrefranca";
console.log("Lastname: ", lastName);

lastName = "Parlutcha";
console.log("Lastname: ", lastName);